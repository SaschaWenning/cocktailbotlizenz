// Hook to translate cocktails
export function useTranslatedCocktails(cocktails: Cocktail[]): TranslatedCocktail[] {
  const { language } = useLanguage()

  return cocktails.map((cocktail) => ({
    ...cocktail,
    name: cocktailTranslations[language][cocktail.id] || cocktail.name,
    description: cocktailDescriptions[language][cocktail.id] || cocktail.description,
    ingredients: cocktail.ingredients.map(
      (ingredient) => ingredientListTranslations[language][ingredient] || ingredient,
    ),
  }))
}

// Hook to translate ingredients
export function useTranslatedIngredients(ingredients: Ingredient[]): TranslatedIngredient[] {
  const { language } = useLanguage()

  return ingredients.map((ingredient) => ({
    ...ingredient,
    name: ingredientTranslations[language][ingredient.id] || ingredient.name,
  }))
}

// Function to translate ingredient name by ID
export function translateIngredientName(ingredientId: string, language: "de" | "en"): string {
  return ingredientTranslations[language][ingredientId] || ingredientId
}

// Function to translate cocktail name by ID
export function translateCocktailName(cocktailId: string, language: "de" | "en"): string {
  return cocktailTranslations[language][cocktailId] || cocktailId
}
